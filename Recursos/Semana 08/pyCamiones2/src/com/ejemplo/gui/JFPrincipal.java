    private void jbBajarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBajarActionPerformed
        int fila = jtCargas.getSelectedRow();
        Carga c = null;
        if (fila != -1) {
            //Si es bidon
            if (!jtCargas.getValueAt(fila, 2).equals("")) {
                c = new Bidon(jtCargas.getValueAt(fila, 0).toString(),
                        Float.parseFloat(jtCargas.getValueAt(fila, 2).toString()),
                        Float.parseFloat(jtCargas.getValueAt(fila, 3).toString()));
            } else if (jtCargas.getValueAt(fila, 4).equals("")==false) {
                c = new Packing(jtCargas.getValueAt(fila, 0).toString(),
                        Float.parseFloat(jtCargas.getValueAt(fila, 5).toString()),
                        Integer.parseInt(jtCargas.getValueAt(fila, 4).toString()),
                        Float.parseFloat(jtCargas.getValueAt(fila, 6).toString()));
            } else {
                c = new Caja(jtCargas.getValueAt(fila, 0).toString(), Float.parseFloat(jtCargas.getValueAt(fila, 1).toString()));
            }
            camion.bajar(c);
            JOptionPane.showMessageDialog(this, "Carga eliminada");
            jcMostrarActionPerformed(null);
        }

    }//GEN-LAST:event_jbBajarActionPerformed


