    private void llenarTabla(ArrayList<Carga> cargas) {
        DefaultTableModel model = new DefaultTableModel();

        model.setColumnIdentifiers(new String[]{"Contenido", "Peso", "Densidad", "Capacidad", "Cantidad Cajas", "Peso x caja", "Peso Estructura"});
        String fila[] = new String[7];
        for (Carga c : cargas) {
            fila[0] = c.getContenido();
            fila[1] = String.valueOf(c.calcularPeso());
            if (c instanceof Packing) {
                Packing packing = (Packing) c;
                fila[2] = "";
                fila[3] = "";
                
                fila[4] = String.valueOf(packing.getCantidad());
                fila[5] = String.valueOf(packing.getPesoCaja());
                fila[6] = String.valueOf(packing.getPesoEstructura());
            } else if (c instanceof Bidon) {
                Bidon bidon = (Bidon) c;
                fila[2] = String.valueOf(bidon.getCapacidad());
                fila[3] = String.valueOf(bidon.getDensidad());;
                fila[4] = "";
                fila[5] = "";
                fila[6] = "";
            } else {
                fila[2] = "";
                fila[3] = "";
                fila[4] = "";
                fila[5] = "";
                fila[6] = "";
            }

            model.addRow(fila);
        }
        jtCargas.setModel(model);
    }
